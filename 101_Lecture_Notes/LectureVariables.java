/* 
    Lecture note example - Variables (String, int, double)
*/

class LectureVariables{
    public static void main(String args[]) {
        String x = new String ("Tamar");
        int y = 31;
        double z = 89.40;
        
        System.out.println(x + " lives in Tennessee.");
        System.out.println(x + " has " + y + " cats.");
        System.out.print(x + " has " + "$" + z + " to feed her " + y + ".");
	}
}